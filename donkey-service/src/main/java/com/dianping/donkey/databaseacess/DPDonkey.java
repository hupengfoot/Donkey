public class DPDonkey {
	private String name;
	private int seq;
	public DPDonkey(String name,int seq){
		this.name = name;
		this.seq = seq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
}
