package com.dianping.donkey.memorytable;


/**
 * define a item of routemap 
 * @author peng.hu
 *
 */
public class RouteTableItem {
	private String domain;
	private String locationTag;
	private String location;
	private String usernameTag;
	private String username;
	private String passwordTag;
	private String tablename;
	private String tablenameTag;
	public String getTablenameTag() {
		return tablenameTag;
	}
	public void setTablenameTag(String tablenameTag) {
		this.tablenameTag = tablenameTag;
	}
	public String getTablename() {
		return tablename;
	}
	public void setTablename(String tablename) {
		this.tablename = tablename;
	}
	public String getLocationTag() {
		return locationTag;
	}
	public void setLocationTag(String locationTag) {
		this.locationTag = locationTag;
	}
	public String getUsernameTag() {
		return usernameTag;
	}
	public void setUsernameTag(String usernameTag) {
		this.usernameTag = usernameTag;
	}
	public String getPasswordTag() {
		return passwordTag;
	}
	public void setPasswordTag(String passwordTag) {
		this.passwordTag = passwordTag;
	}
	private String password;
	private String type;
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
}
